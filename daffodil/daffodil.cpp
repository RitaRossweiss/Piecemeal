// 输出100～999中的所有水仙花数。若3位数ABC满足ABC＝A3＋B3＋C3，则称其为水仙花 数。例如153＝13＋53＋33，所以153是水仙花数。
// 经典问题，各种教科书上貌似总会有这个题。
#include<stdio.h>
int main(){
    int a,b,c;
    for (int i = 100; i <= 999; i++)
    {
        a = i/100%10; //获得百位
        b = i/10%10; //获得十位
        c = i%10; //获得个位
        if (a*a*a+b*b*b+c*c*c == i)
        {
            printf("%d\n",i);
        }
    }
    return 0;
}